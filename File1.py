

def add(a: int, b: int) -> int:
    """
    Adds two numbers.

    Args:
        a (int): The first number.
        b (int): The second number.

    Returns:
        int: The sum of a and b.

    Example:
        >>> add(2, 3)
        5
    """
    return a + b


def subtract(a: int, b: int) -> int:
    """
    Subtracts one number from another.

    Args:
        a (int): The first number.
        b (int): The second number.

    Returns:
        int: The result of a - b.

    Example:
        >>> subtract(5, 3)
        2
    """
    return a - b
